package com.springmvc.sample;


public class Passenger {
	
	private String name;
	
	private String text;
	
	public Passenger(String name, String text) {
		this.name = name;
		this.text = text;
	}

	public String getName() {
		return name;
	}

	public String getText() {
		return text;
	}

	public String getTranslatedText() {
		return text;
	}

}
